package Week2.twelvth;

public class EqualityOperatorsExample {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("Equality Operators:");
        System.out.println("a == 10: " + (a == 10)); // true
        System.out.println("b != 20: " + (b != 20)); // true
    }
}
