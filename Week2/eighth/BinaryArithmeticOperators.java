package Week2.eighth;

public class BinaryArithmeticOperators {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 4;

        System.out.println("Binary Arithmetic Operators:");

        // Addition
        System.out.println("Addition (num1 + num2): " + (num1 + num2)); // 19

        // Subtraction
        System.out.println("Subtraction (num1 - num2): " + (num1 - num2)); // 11

        // Multiplication
        System.out.println("Multiplication (num1 * num2): " + (num1 * num2)); // 60

        // Division
        System.out.println("Division (num1 / num2): " + (num1 / num2)); // 3

        // Modulus
        System.out.println("Modulus (num1 % num2): " + (num1 % num2)); // 3
    }
}