package Week2.tenth;

public class UnaryOperators {
    public static void main(String[] args) {
        int num = 10;

        // Unary Plus
        System.out.println("Unary Plus: " + (+num)); // 10

        // Unary Minus
        System.out.println("Unary Minus: " + (-num)); // -10

        // Logical Complement
        boolean flag = true;
        System.out.println("Logical Complement: " + (!flag)); // false
    }
}
