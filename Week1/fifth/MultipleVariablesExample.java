package fifth;

public class MultipleVariablesExample {
    public static void main(String[] args) {
        // Declaring multiple int variables
        int a = 5, b = 10, c = 15;

        // Declaring multiple double variables
        double x = 1.5, y = 2.5, z = 3.5;

        // Printing the values
        System.out.println("Integers: " + a + ", " + b + ", " + c);
        System.out.println("Doubles: " + x + ", " + y + ", " + z);
    }
}
